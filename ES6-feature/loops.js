// there are generally three types of loops that are used and they are for loop, while loop and do-whileloop;

//for loop 

console.log("Example of for loop");
for (let i = 1; i < 5; i++) {
  console.log("Number:", i);
}


//while loop(entry control loop)

console.log("Example of while loop");
let j = 1;
while (j < 5) {
  console.log("Number:", j);
  j++;
}

// do-while loop(exit control loop)
console.log("Example of do-while loop");
let k = 0;
do {
  console.log("Number:", k);
  k++;
} while (k < 5);
